// ─────────────────────────────────────────────────────────────────────────────
// Baseline de Core Web Vitals de laboratório (CLAUDE.md §11, Phase 2 item 8).
//
// Roda o Lighthouse contra PRODUÇÃO, uma vez por URL do sitemap, e escreve um
// relatório versionável com as quatro categorias e as métricas dos budgets §6.
//
// Diferença para `lighthouserc.js`: aquele é o portão de merge — roda contra
// `next start` local, 3 execuções, throttling devtools, e reprova o build. Este
// é o retrato datado da produção, que vira a linha "antes" do experiment log.
// As duas medições não são comparáveis entre si; compare baseline com baseline.
//
//   node scripts/baseline-lighthouse.mjs
//   node scripts/baseline-lighthouse.mjs --base http://localhost:3000 --out /tmp/x
// ─────────────────────────────────────────────────────────────────────────────

import fs from 'node:fs'
import os from 'node:os'
import path from 'node:path'
import { execFileSync } from 'node:child_process'

const argv = process.argv.slice(2)
const arg = (name, fallback) => {
  const i = argv.indexOf(`--${name}`)
  return i === -1 ? fallback : argv[i + 1]
}

const BASE = arg('base', 'https://seotecnico.dev.br').replace(/\/$/, '')
const TODAY = new Date().toLocaleDateString('sv-SE')
const OUT = arg('out', path.join('docs', 'baseline', TODAY))

// Budgets do CLAUDE.md §6 — os mesmos que o lighthouserc.js aplica no CI.
const BUDGETS = {
  performance: { min: 95, label: 'Performance ≥ 95' },
  lcp: { max: 2000, label: 'LCP < 2.0s' },
  cls: { max: 0.05, label: 'CLS < 0.05' },
  tbt: { max: 200, label: 'TBT < 200ms' },
}

const lighthouseBin = path.join(
  'node_modules',
  '.bin',
  process.platform === 'win32' ? 'lighthouse.cmd' : 'lighthouse',
)

function sitemapPaths() {
  return fetch(`${BASE}/sitemap.xml`)
    .then((r) => r.text())
    .then((xml) =>
      [...xml.matchAll(/<loc>([^<]+)<\/loc>/g)].map((m) => new URL(m[1]).pathname),
    )
}

function runLighthouse(url, reportPath) {
  execFileSync(
    lighthouseBin,
    [
      url,
      '--only-categories=performance,seo,accessibility,best-practices',
      '--chrome-flags=--headless=new --no-sandbox',
      '--output=json',
      `--output-path=${reportPath}`,
      '--quiet',
    ],
    { stdio: ['ignore', 'ignore', 'inherit'], shell: process.platform === 'win32' },
  )
  return JSON.parse(fs.readFileSync(reportPath, 'utf8'))
}

/** Métricas e categorias de um relatório bruto do Lighthouse. */
function summarize(report) {
  const score = (id) => Math.round(report.categories[id].score * 100)
  const numeric = (id) => report.audits[id].numericValue

  const failingA11y = report.categories.accessibility.auditRefs
    .map((ref) => report.audits[ref.id])
    .filter((audit) => audit.score !== null && audit.score < 1)
    .map((audit) => audit.id)

  return {
    path: new URL(report.finalDisplayedUrl).pathname.replace(/\/$/, '') || '/',
    performance: score('performance'),
    seo: score('seo'),
    accessibility: score('accessibility'),
    bestPractices: score('best-practices'),
    lcp: numeric('largest-contentful-paint'),
    fcp: numeric('first-contentful-paint'),
    cls: numeric('cumulative-layout-shift'),
    tbt: numeric('total-blocking-time'),
    failingA11y,
  }
}

function withinBudget(row) {
  return (
    row.performance >= BUDGETS.performance.min &&
    row.lcp < BUDGETS.lcp.max &&
    row.cls < BUDGETS.cls.max &&
    row.tbt < BUDGETS.tbt.max
  )
}

function toMarkdown(rows, meta) {
  const s = (ms) => `${(ms / 1000).toFixed(1)}s`
  const failed = rows.filter((r) => !withinBudget(r))
  const lines = []

  lines.push(`# Lighthouse baseline — ${meta.capturedAt}`)
  lines.push('')
  lines.push(`> Generated by \`npm run baseline:lighthouse\` against \`${meta.base}\`.`)
  lines.push(`> Lighthouse ${meta.lighthouseVersion}, mobile emulation, simulated throttling,`)
  lines.push('> **one run per URL** — a single run, unlike the 3-run median the CI gate uses,')
  lines.push('> so treat ±3 points of Performance as noise. Machine: ' + meta.machine + '.')
  lines.push('')
  lines.push(
    failed.length === 0
      ? `**All ${rows.length} URLs are inside the CLAUDE.md §6 budgets.**`
      : `**${failed.length} of ${rows.length} URLs are outside the §6 budgets.**`,
  )
  lines.push('')

  lines.push('## Scores')
  lines.push('')
  lines.push('| Path | Performance | SEO | Accessibility | Best practices |')
  lines.push('|---|---|---|---|---|')
  for (const r of rows) {
    lines.push(`| \`${r.path}\` | ${r.performance} | ${r.seo} | ${r.accessibility} | ${r.bestPractices} |`)
  }
  lines.push('')

  lines.push('## Metrics vs. budgets')
  lines.push('')
  lines.push(`Budgets: ${Object.values(BUDGETS).map((b) => b.label).join(' · ')}`)
  lines.push('')
  lines.push('| Path | LCP | FCP | CLS | TBT | Within budget |')
  lines.push('|---|---|---|---|---|---|')
  for (const r of rows) {
    lines.push(
      `| \`${r.path}\` | ${s(r.lcp)} | ${s(r.fcp)} | ${r.cls.toFixed(3)} | ${Math.round(r.tbt)}ms | ${withinBudget(r) ? 'yes' : '**no**'} |`,
    )
  }
  lines.push('')

  const withA11yIssues = rows.filter((r) => r.failingA11y.length > 0)
  lines.push('## Failing accessibility audits')
  lines.push('')
  if (withA11yIssues.length === 0) {
    lines.push('None.')
  } else {
    lines.push('| Path | Audit |')
    lines.push('|---|---|')
    for (const r of withA11yIssues) {
      lines.push(`| \`${r.path}\` | ${r.failingA11y.join(', ')} |`)
    }
  }
  lines.push('')

  return lines.join('\n')
}

// ── Execução ────────────────────────────────────────────────────────────────

const paths = await sitemapPaths()
const reportsDir = fs.mkdtempSync(path.join(os.tmpdir(), 'lh-baseline-'))
const rows = []
let lighthouseVersion = ''

for (const [i, p] of paths.entries()) {
  const url = `${BASE}${p === '/' ? '' : p}`
  process.stderr.write(`  [${i + 1}/${paths.length}] ${url}\n`)
  const report = runLighthouse(url, path.join(reportsDir, `${i}.json`))
  lighthouseVersion = report.lighthouseVersion
  rows.push(summarize(report))
}

fs.mkdirSync(OUT, { recursive: true })
fs.writeFileSync(
  path.join(OUT, 'lighthouse.md'),
  toMarkdown(rows, {
    capturedAt: TODAY,
    base: BASE,
    lighthouseVersion,
    machine: `${os.platform()} ${os.arch()}, ${os.cpus().length} cores`,
  }),
)
fs.rmSync(reportsDir, { recursive: true, force: true })

const failed = rows.filter((r) => !withinBudget(r)).length
console.log(`[baseline] ${rows.length} URLs → ${OUT}/lighthouse.md (${failed} outside budget)`)
